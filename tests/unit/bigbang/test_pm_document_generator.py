class TestPMDocumentGeneratorGenerate:
    """Tests for PMDocumentGenerator.generate()."""

    @pytest.mark.asyncio
    async def test_generate_with_qa_pairs_returns_llm_content(self):
        """LLM-generated content is returned when call succeeds."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        result = await generator.generate(seed, qa_pairs=_sample_qa_pairs())

        assert result.is_ok
        assert "Task Manager" in result.value
        # Verify LLM was called
        adapter.complete.assert_awaited_once()

    @pytest.mark.asyncio
    async def test_generate_without_qa_pairs(self):
        """Generate works with seed only (no Q&A transcript)."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        result = await generator.generate(seed)

        assert result.is_ok
        adapter.complete.assert_awaited_once()

    @pytest.mark.asyncio
    async def test_generate_extracts_qa_from_interview_state(self):
        """Q&A pairs are extracted from InterviewState when provided."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        # Mock InterviewState with rounds
        mock_state = MagicMock()
        mock_round1 = MagicMock()
        mock_round1.question = "What problem?"
        mock_round1.user_response = "Task tracking."
        mock_round2 = MagicMock()
        mock_round2.question = "Who uses it?"
        mock_round2.user_response = "Developers."
        mock_state.rounds = [mock_round1, mock_round2]

        result = await generator.generate(seed, interview_state=mock_state)

        assert result.is_ok
        # Verify the prompt includes Q&A from state
        call_args = adapter.complete.call_args
        messages = call_args[0][0]
        user_msg = messages[1].content
        assert "What problem?" in user_msg
        assert "Task tracking." in user_msg

    @pytest.mark.asyncio
    async def test_generate_qa_pairs_take_precedence_over_state(self):
        """Explicit qa_pairs take precedence over interview_state."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        mock_state = MagicMock()
        mock_round = MagicMock()
        mock_round.question = "State question"
        mock_round.user_response = "State answer"
        mock_state.rounds = [mock_round]

        explicit_pairs = [("Explicit Q", "Explicit A")]

        result = await generator.generate(
            seed,
            qa_pairs=explicit_pairs,
            interview_state=mock_state,
        )

        assert result.is_ok
        call_args = adapter.complete.call_args
        user_msg = call_args[0][0][1].content
        assert "Explicit Q" in user_msg
        assert "State question" not in user_msg

    @pytest.mark.asyncio
    async def test_generate_falls_back_to_template_on_llm_error(self):
        """Falls back to template-based generation when LLM fails."""
        adapter = _make_failing_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        result = await generator.generate(seed, qa_pairs=_sample_qa_pairs())

        assert result.is_ok
        # Should contain template-generated content
        content = result.value
        assert "# Task Manager" in content
        assert "## Goal" in content

    @pytest.mark.asyncio
    async def test_generate_falls_back_on_too_short_response(self):
        """Falls back to template when LLM returns very short content."""
        adapter = _make_adapter(content="OK")  # Too short
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        result = await generator.generate(seed)

        assert result.is_ok
        # Should contain template content, not the short "OK"
        assert "## Goal" in result.value

    @pytest.mark.asyncio
    async def test_generate_falls_back_on_empty_response(self):
        """Falls back to template when LLM returns empty content."""
        adapter = _make_adapter(content="")
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        result = await generator.generate(seed)

        assert result.is_ok
        assert "# Task Manager" in result.value


