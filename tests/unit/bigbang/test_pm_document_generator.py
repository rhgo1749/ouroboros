class TestPMDocumentGeneratorMessages:
    """Tests for the LLM message structure sent by generate()."""

    @pytest.mark.asyncio
    async def test_system_message_contains_pm_instructions(self):
        """System prompt instructs LLM to write a PM."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        await generator.generate(seed, qa_pairs=_sample_qa_pairs())

        call_args = adapter.complete.call_args
        messages = call_args[0][0]

        assert len(messages) == 2
        assert messages[0].role.value == "system"
        assert "Product Requirements Document" in messages[0].content
        assert messages[1].role.value == "user"

    @pytest.mark.asyncio
    async def test_completion_config_parameters(self):
        """Completion config uses appropriate parameters for document generation."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter, model="test-model")
        seed = _make_seed()

        await generator.generate(seed)

        call_args = adapter.complete.call_args
        config = call_args[0][1]

        assert config.model == "test-model"
        assert config.model_is_explicit is True
        assert config.temperature == 0.3
        assert config.max_tokens == 8192

    @pytest.mark.asyncio
    async def test_default_model_path_keeps_document_model_implicit(self):
        """Implicit document model should preserve downstream profile-based overrides."""
        adapter = _make_adapter()
        seed = _make_seed()

        with patch(
            "ouroboros.bigbang.pm_document.get_clarification_model",
            return_value="default",
        ):
            generator = PMDocumentGenerator(llm_adapter=adapter)
            await generator.generate(seed)

        config = adapter.complete.call_args[0][1]
        assert config.model == "default"
        assert config.model_is_explicit is False

    @pytest.mark.asyncio
    async def test_user_message_includes_all_seed_sections(self):
        """User message includes all populated seed sections."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed(
            brownfield_repos=({"path": "/repo", "name": "Repo", "desc": "Test"},),
            codebase_context="Python FastAPI project.",
        )
        qa = [("Q1", "A1")]

        await generator.generate(seed, qa_pairs=qa)

        user_msg = adapter.complete.call_args[0][0][1].content

        assert "Extracted Requirements" in user_msg
        assert "Task Manager" in user_msg
        assert "Full Interview Transcript" in user_msg
        assert "Q1" in user_msg
        assert "Repo" in user_msg
        # Codebase analysis is excluded from PM prompts
        assert "Python FastAPI project." not in user_msg
