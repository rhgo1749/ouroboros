"""Unit tests for PMDocumentGenerator — LLM-based PM generation.

Tests the PMDocumentGenerator class which uses an LLM to generate a rich
pm.md from full Q&A transcript plus PMSeed, with fallback to template-based
generation on failure.
"""

from pathlib import Path
from unittest.mock import AsyncMock, MagicMock, patch

import pytest

from ouroboros.bigbang.pm_document import (
    PMDocumentGenerator,
)
from ouroboros.bigbang.pm_seed import PMSeed, UserStory
from ouroboros.core.errors import ProviderError
from ouroboros.core.types import Result
from ouroboros.providers.base import (
    CompletionResponse,
    UsageInfo,
)

# ──────────────────────────────────────────────────────────────────
# Helpers
# ──────────────────────────────────────────────────────────────────

_SAMPLE_PM_MD = """\
# Task Manager

*Generated by Ouroboros PM Generator*

## Goal

Build a task management tool for small teams.

## User Stories

1. **As a** team lead, **I want to** assign tasks, **so that** work is distributed.

## Constraints

- Must launch within 3 months

## Success Criteria

1. 80% user adoption in first quarter

---
*PM ID: pm_seed_test123*
*Interview ID: int_abc*
"""


def _make_seed(**overrides) -> PMSeed:
    """Create a PMSeed with sensible defaults for testing."""
    defaults = {
        "pm_id": "pm_seed_test123",
        "product_name": "Task Manager",
        "goal": "Build a task management tool for small teams.",
        "user_stories": (
            UserStory(
                persona="team lead",
                action="assign tasks",
                benefit="work is distributed",
            ),
        ),
        "constraints": ("Must launch within 3 months",),
        "success_criteria": ("80% user adoption in first quarter",),
        "decide_later_items": ("API rate limiting", "Which database to use?"),
        "assumptions": ("Team has access to cloud infra",),
        "interview_id": "int_abc",
    }
    defaults.update(overrides)
    return PMSeed(**defaults)


def _make_adapter(content: str = _SAMPLE_PM_MD) -> MagicMock:
    """Create a mock LLM adapter returning the given content."""
    adapter = MagicMock()
    adapter.complete = AsyncMock(
        return_value=Result.ok(
            CompletionResponse(
                content=content,
                model="claude-opus-4-6",
                usage=UsageInfo(
                    prompt_tokens=500,
                    completion_tokens=300,
                    total_tokens=800,
                ),
            )
        )
    )
    return adapter


def _make_failing_adapter() -> MagicMock:
    """Create a mock adapter that returns an error."""
    adapter = MagicMock()
    adapter.complete = AsyncMock(return_value=Result.err(ProviderError("LLM service unavailable")))
    return adapter


def _sample_qa_pairs() -> list[tuple[str, str]]:
    """Return sample Q&A pairs for testing."""
    return [
        ("What problem does this solve?", "Our team needs better task tracking."),
        ("Who are the primary users?", "Team leads and developers."),
        ("What are your success metrics?", "80% adoption in Q1."),
    ]


