class TestPMDocumentGeneratorSave:
    """Tests for PMDocumentGenerator.save()."""

    def test_save_writes_to_default_dir(self, tmp_path: Path, monkeypatch):
        """Save writes pm.md to .ouroboros/ by default."""
        monkeypatch.chdir(tmp_path)
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        path = generator.save("# My PM", seed)

        assert path.exists()
        assert path.name == "pm.md"
        assert path.parent.name == ".ouroboros"
        assert path.read_text() == "# My PM"

    def test_save_writes_to_custom_dir(self, tmp_path: Path):
        """Save writes pm.md to custom output directory."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        custom_dir = tmp_path / "custom"
        path = generator.save("# Custom PM", seed, output_dir=custom_dir)

        assert path.exists()
        assert path == custom_dir / "pm.md"
        assert path.read_text() == "# Custom PM"

    def test_save_creates_directories(self, tmp_path: Path):
        """Save creates parent directories if they don't exist."""
        adapter = _make_adapter()
        generator = PMDocumentGenerator(llm_adapter=adapter)
        seed = _make_seed()

        nested_dir = tmp_path / "a" / "b" / "c"
        path = generator.save("# Nested", seed, output_dir=nested_dir)

        assert path.exists()
        assert path.read_text() == "# Nested"


