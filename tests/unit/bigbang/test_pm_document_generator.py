class TestPMDocumentGeneratorPrompt:
    """Tests for the prompt building logic."""

    def test_prompt_includes_seed_fields(self):
        """Prompt includes all relevant PMSeed fields."""
        seed = _make_seed()
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "Task Manager" in prompt
        assert "Build a task management" in prompt
        assert "team lead" in prompt
        assert "assign tasks" in prompt
        assert "Must launch within 3 months" in prompt
        assert "80% user adoption" in prompt
        assert "API rate limiting" in prompt
        assert "Which database to use?" in prompt
        assert "Team has access to cloud infra" in prompt

    def test_prompt_includes_qa_pairs(self):
        """Prompt includes the full Q&A transcript."""
        seed = _make_seed()
        qa = _sample_qa_pairs()
        prompt = PMDocumentGenerator._build_generation_prompt(seed, qa)

        assert "## Full Interview Transcript" in prompt
        assert "**Q1:**" in prompt
        assert "**A1:**" in prompt
        assert "What problem does this solve?" in prompt
        assert "Our team needs better task tracking." in prompt

    def test_prompt_omits_transcript_section_when_no_qa(self):
        """Transcript section is omitted when no Q&A pairs provided."""
        seed = _make_seed()
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "## Full Interview Transcript" not in prompt

    def test_prompt_includes_brownfield_repos(self):
        """Prompt includes brownfield repo information."""
        seed = _make_seed(
            brownfield_repos=({"path": "/code/myapp", "name": "MyApp", "desc": "Main app"},),
        )
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "MyApp" in prompt
        assert "/code/myapp" in prompt
        assert "Main app" in prompt

    def test_prompt_excludes_codebase_context(self):
        """Prompt excludes codebase analysis (technical detail not for PMs)."""
        seed = _make_seed(codebase_context="Uses FastAPI with PostgreSQL backend.")
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "Codebase Analysis" not in prompt
        assert "Uses FastAPI with PostgreSQL backend." not in prompt

    def test_prompt_handles_empty_seed(self):
        """Prompt handles a minimal seed with empty fields gracefully."""
        seed = PMSeed()
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "## Extracted Requirements" in prompt
        assert "Not specified" in prompt  # goal fallback
        assert "Unnamed" in prompt  # product_name fallback

    def test_prompt_omits_empty_pm_id_and_interview_id(self):
        """Prompt omits PM ID and Interview ID lines when values are empty."""
        seed = PMSeed(pm_id="", interview_id="")
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "**PM ID:**" not in prompt
        assert "**Interview ID:**" not in prompt

    def test_prompt_includes_pm_id_when_present(self):
        """Prompt includes PM ID when it has a value."""
        seed = _make_seed()
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "**PM ID:** pm_seed_test123" in prompt
        assert "**Interview ID:** int_abc" in prompt

    def test_prompt_brownfield_repo_empty_name_uses_path(self):
        """Prompt builder falls back to path when brownfield repo name is empty, without duplicate."""
        seed = _make_seed(
            brownfield_repos=({"name": "", "path": "/repo/path", "desc": "desc"},),
        )
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        # Name falls back to path, so parenthetical path is NOT shown
        assert "/repo/path" in prompt
        assert "/repo/path (/repo/path)" not in prompt
        assert "desc" in prompt

    def test_prompt_brownfield_repo_none_name_uses_path(self):
        """Prompt builder falls back to path when brownfield repo name is None."""
        seed = _make_seed(
            brownfield_repos=({"name": None, "path": "/repo/path"},),
        )
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "/repo/path" in prompt

    def test_prompt_brownfield_repo_missing_path(self):
        """Prompt builder handles brownfield repo with missing path."""
        seed = _make_seed(
            brownfield_repos=({"name": "MyApp"},),
        )
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert "MyApp ()" not in prompt
        assert "MyApp" in prompt

    def test_prompt_generation_instruction_at_end(self):
        """Prompt ends with the generation instruction."""
        seed = _make_seed()
        prompt = PMDocumentGenerator._build_generation_prompt(seed)

        assert prompt.strip().endswith(
            "Generate a polished PM document from the above requirements and interview transcript."
        )


